# Get the input from the user
number1 = int(input("Enter a number: "))
number2 = int(input("Enter another number: "))

# Calculate the sum
sum = number1 + number2

# Calculate the average
average = (number1 + number2) / 2

# Print the results
print("The sum of the two numbers is:", sum)
print("The average of the two numbers is:", average)

# This program first fetches the two numbers number1 and number2.
# It then calculates the sum of these two numbers using the + operator,
# and stores the result in the variable sum. It then calculates the average
# of the two numbers by dividing their sum by 2, and stores the result in the
# variable average. Finally, it prints the results to the console using the
# print() function.