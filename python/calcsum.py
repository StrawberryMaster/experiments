# Get the input from the user
num1 = input("Enter a number: ")
num2 = input("Enter another number: ")

# Convert the input to integers
num1 = int(num1)
num2 = int(num2)

# Calculate the sum of the two numbers
sum = num1 + num2

# Print the result
print("The sum of the two numbers is:", sum)

